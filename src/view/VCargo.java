    private void txSalarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txSalarioKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_txSalarioKeyTyped

