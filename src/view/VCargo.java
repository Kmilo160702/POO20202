    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        objetos.remove(objeto);
        util.serializar(NOM_ARCHIVO, objetos);
        getObjetos(false, true, false);
    }//GEN-LAST:event_jbBorrarActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed

        objeto.setDescripcion(txDescripcion.getText());
        objeto.setNombreCargo(txNombreCargo.getText());
        objeto.setSalario(Double.parseDouble(txSalario.getText()));

        objetos.remove(objeto);
        objetos.add(objeto);
        util.serializar(NOM_ARCHIVO, objetos);
        getObjetos(false, true, false);
    }//GEN-LAST:event_jbGuardarActionPerformed

    private void ListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListadoMouseClicked
        objeto = (Cargo) this.Listado.getValueAt(Listado.getSelectedRow(), 0);

        /////
        this.txDescripcion.setText(objeto.getDescripcion());
        this.txNombreCargo.setText(objeto.getNombreCargo());
        this.txSalario.setText(String.valueOf(objeto.getSalario()));

        setEstadoBotones(true, true, true);
    }//GEN-LAST:event_ListadoMouseClicked

